package main

import (
	"Ag4AcSync/GeneticAlgorithm"
	"fmt"
	"sync"
	"time"
)

var ranges [8]float64
var numbers []int
var wg sync.WaitGroup
var mux sync.Mutex
var winners []GeneticAlgorithm.Individual

func Exec(n int) {
	var BestIndividual *GeneticAlgorithm.Individual

	for e := 0; e < n; e++ {
		fmt.Println("EXECUÇÃO: ", e+1)

		Ag := GeneticAlgorithm.GeneticAlgorithm{}
		Ag.Init(100, 100, .016, .1, 3)
		Ag.Run()

		grids := Ag.GenerateGrids(10000, true)

		BestIndex := 0
		BestFitnessPercentage := 0.0

		//println("INICIANDO AVALIAÇÃO EM 10.000")
		for i := 0; i < Ag.ElitismSize; i++ {
			Ag.Individuals[i].CalcFitness(grids)

			//fmt.Println(i+1, ":", *Ag.Individuals[i].DNA, "||", Ag.Individuals[i].Fitness, "||", 100*float64(Ag.Individuals[i].Fitness)/10000.0, "%")

			if Ag.Individuals[i].Fitness > Ag.Individuals[BestIndex].Fitness {
				BestIndex = i
			}
		}

		BestIndividual = &Ag.Individuals[BestIndex]
		BestFitnessPercentage = 100 * float64(BestIndividual.Fitness) / 10000.0

		fmt.Println("WINNER OF EXEC", e+1, ":", BestIndividual.DNA, "||", BestIndividual.Fitness, "||", 100*float64(BestIndividual.Fitness)/10000.0, "%")

		mux.Lock()
		for i := 0; i < 8; i++ {
			if BestFitnessPercentage < ranges[i] {
				numbers[i]++
				break
			}
		}

		winners = append(winners, *BestIndividual)
		mux.Unlock()
	}
	wg.Done()
}

func main() {
	ranges = [8]float64{40.0, 50.0, 60.0, 70.0, 80.0, 90.0, 100.0, 101}
	numbers = make([]int, 8)

	for k := 1; k <= 5; k++ {
		start := time.Now()
		for i := 0; i < k; i++ {
			wg.Add(1)
			go Exec(1)
		}

		wg.Wait()
		elapsed := time.Since(start)

		fmt.Println("TIME: ", elapsed, "||")
	}

	fmt.Println("END")
	fmt.Println(ranges)
	fmt.Println(numbers)

	for index, element := range winners {
		fmt.Println(index, ":", element.DNA, "||", 100*float64(element.Fitness)/10000.0, "%")
	}
}

